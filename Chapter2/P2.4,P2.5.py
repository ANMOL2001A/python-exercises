num1 = int(input("Enter the first integer: "))
num2 = int(input("Enter the second integer: "))
sum_result = num1 + num2
difference_result = num1 - num2
product_result = num1 * num2
average_result = (num1 + num2) / 2
distance_result = abs(num1 - num2)
maximum_result = max(num1, num2)
minimum_result = min(num1, num2)
print(f"Sum:\t{sum_result}")
print(f"Difference:\t{difference_result}")
print(f"Product:\t{product_result}")
print(f"Average:\t{average_result}")
print(f"Distance:\t{distance_result}")
print(f"Maximum:\t{maximum_result}")
print(f"Minimum:\t{minimum_result}")

